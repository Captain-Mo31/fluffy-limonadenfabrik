<div class="text-black dark:text-white">
  Fluffy GmbH ist ein junges, innovatives Unternehmen mit zur Zeit 34
  Mitarbeitern, das alkoholfreie Erfrischungsgetränke ausschließlich biologisch
  herstellt.
  <br />
  Das Unternehmen hat seinen Sitz in Hamburg Altona. Es ist aus der ehemaligen Altonaer-Traditionsbrauerei
  hervorgegangen. Die Basis all unserer Getränke ist das wertvolle Mineralwasser
  aus dem Harz. Diese besondere Quelle zeichnet sich durch besondere Reinheit und
  vielen Spurenelementen.
  <br />
  Die Marke Fluffy als Garant für leckere Limonadensorten, wurde 2001 entwickelt
  und hat erfreut sich seitdem wachsender Beliebtheit.
  <br />
  Die Getränkesorten der Marke Fluffy sind alle rein biologisch hergestellt und durch
  enthalten keinen zusätzlichen Zucker. Sie wird somit besonders von ernährungsbewußten
  Kunden bevorzugt.
  <br />
  Fluffy GmbH ist auf dem Weg, seine Produkte deutschlandweit zu vertreiben. Hierzu
  sind wir gerade dabei ein bundesweites Vertriebsnetz aufzubauen.
