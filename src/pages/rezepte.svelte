<h1>Rezepte:</h1>

<div>
  <p>
    Fluffy-Limonade entspricht dem Trend nach natürlicher und gesunder
    Ernährung: Ein alkoholfreies Erfrischungsgetränk, das durch auf natürliche
    Art hergestellt wird. Fluffy-Limonade ist von Haus aus calciumreich aber
    natriumarm. Mit den enthaltenen Mineralien stellt es ein wichtigen Baustein
    zur gesunden Ernährung dar, und dieses Tag für Tag Jüngste
    Forschungsergebnisse haben gezeigt, dass Fluffylimonade überaus geeignet ist
    die tägliche Versorgung mit Mineralstoffen und Spurenelementen
    sicherzustellen.
  </p>
  <br />
  <h3>Der magische Trunk</h3>
  <p>
    Hierfür werden eine frische Apfelsine, ein Apfel, ein wenig Zimt, etwas
    Ingwer und Fluffy Orangenlimonade gebraucht. Apfelsine und Apfel schälen und
    klein schneiden und mit der Fluffy Orangenlimonade verrühren. Zum Abschluss
    mit Zimt und Ingwer verfeinern.
  </p>
  <br />
  <h3>Der schnelle Läufer</h3>

  <p>
    Hierfür benötigen Sie eine Limette, etwas Kümmel und Fluffy Bitterlemon.
    Limette waschen und kleinschneiden, etwas Kümmel hinzugeben und zum
    Abschluss mit Fluffy Bitterlemon auffüllen. Mmmh, das schmeckt und gibt
    starke Beine.
  </p>
  <br />
  <h3>Der Erfrischer</h3>

  <p>
    Hierfür benötigen Sie eine Zitrone, viel Eis und eine Dose
    Fluffygetränkedose (Zitrone oder Orange). Zitrone durchschneiden und
    ausdrücken, Eis bis zum Glasrand und zum Abschluss mit Fluffy aufgießen. Das
    erfrischt für den ganzen Tag!
  </p>
</div>
